package buiduyanh.fpoly.ph30569_and1_ot.Model;

public class GiaoVien {
    private String magv;
    private String ten;

    public GiaoVien() {
    }

    public GiaoVien(String magv, String ten) {
        this.magv = magv;
        this.ten = ten;
    }

    public String getMagv() {
        return magv;
    }

    public void setMagv(String magv) {
        this.magv = magv;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }
}
