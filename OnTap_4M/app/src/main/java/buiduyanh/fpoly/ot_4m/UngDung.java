package buiduyanh.fpoly.ot_4m;

public class UngDung {

    private String muc1;
    private String muc2;
    private String muc3;
    private String muc4;


    public UngDung(String muc1, String muc2, String muc3, String muc4) {
        this.muc1 = muc1;
        this.muc2 = muc2;
        this.muc3 = muc3;
        this.muc4 = muc4;
    }


    public String getMuc1() {
        return muc1;
    }

    public void setMuc1(String muc1) {
        this.muc1 = muc1;
    }

    public String getMuc2() {
        return muc2;
    }

    public void setMuc2(String muc2) {
        this.muc2 = muc2;
    }

    public String getMuc3() {
        return muc3;
    }

    public void setMuc3(String muc3) {
        this.muc3 = muc3;
    }

    public String getMuc4() {
        return muc4;
    }

    public void setMuc4(String muc4) {
        this.muc4 = muc4;
    }
}
