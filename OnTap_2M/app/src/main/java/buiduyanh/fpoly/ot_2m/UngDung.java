package buiduyanh.fpoly.ot_2m;

public class UngDung {

    private String muc1;
    private String muc2;


    public UngDung(String muc1, String muc2) {
        this.muc1 = muc1;
        this.muc2 = muc2;

    }


    public String getMuc1() {
        return muc1;
    }

    public void setMuc1(String muc1) {
        this.muc1 = muc1;
    }

    public String getMuc2() {
        return muc2;
    }

    public void setMuc2(String muc2) {
        this.muc2 = muc2;
    }

}
